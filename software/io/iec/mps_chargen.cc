/* =======  Chargen Tables */

        /*-
         *
         *  In theses tables each item is an array of 12 values
         *  Values 0-10 are the 11 charecter column graphic bitmap.
         *  LSB is up. A character is defined in a 12x8 matrix
         *  last column is usually empty (see meaning of index 11)
         *
         *  Example for the A character in the DRAFT table (item 46)
         *
         *             78h 04h 12h 01h 10h 01h 12h 04h 78h 00h 00h
         *      1       .   .   .   O   .   O   .   .   .   .   .
         *      2       .   .   O   .   .   .   O   .   .   .   .
         *      4       .   O   .   .   .   .   .   O   .   .   .
         *      8       O   .   .   .   .   .   .   .   O   .   .
         *      16      O   .   O   .   O   .   O   .   O   .   .
         *      32      O   .   .   .   .   .   .   .   O   .   .
         *      64      O   .   .   .   .   .   .   .   O   .   .
         *      128     .   .   .   .   .   .   .   .   .   .   .
         *
         *  Constraint for the 9 dot matrix printer in DRAFT mode was :
         *  it's not allowed to have the same dot ON in 2 consecutive
         *  rows to let the head recycle. This emulator tries to reproduce
         *  this constraint to calculate reverse mode matrix.
         *
         *  The last value at index 11 stores attribute bits :
         *  Bit 0=0 : Use the 8 higher dots of the printer head
         *  Bit 0=1 : Use the 8 lower dots of the printer head
         *  Nit 3=0 : column 12 is empty
         *  Nit 3=1 : column 12 is identical to column 11 (only used on NLQ)
         *  Bit 7=0 : Standard character
         *  Bit 7=1 : Tall character
         *  Bits 4,5,6 : encoded value (0 to 7) of a chargen of the same table
         *  that needs to be printed just below the character if tall bit is set
         *  (mainly used by IBM semi-graphic characters to draw table lines)
         *
         *  Bits 1 and 2 are unused and reserved for future use.
         *
        -*/

